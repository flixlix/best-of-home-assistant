export default function createEmojisFromString(string: string): string {
  if (typeof string !== "string" || string?.replace === undefined) return string;
  const regex = /:([^:\s]+(\s+[^:\s]+)*):/g;
  return string.replace(regex, (match, emojiName) => {
    const emoji = emojis[emojiName];
    return emoji ? emoji : match;
  });
}
